				<h1 class="title">Photomontage</h1>
				<p>Collectively developed by the Berlin Dada group, photomontage is a variation on the collage technique, in which pasted items are actual photographs or photographic reproductions culled from the press. The appropriation of the mass media provided endless fodder for the Dadaists scathing critiques. The disjunctive cuts of photomontage effectively captured the fissures and shocks of modernity.</p> 

				<h1 class="title">L’Ashby’s photomontage</h1>
				<p>In true absurdist style, Madam L’Ashby, substituting scissors and a glue stick for brushes and paint, employs photomontage combining individual photographs to create a new subject or visual image. The work contains elements at once real and imaginary, a metaphorical ‘cutting up of society’ and critique of the celebrity culture in which we live. The works also reference Glomesh, Madam L’Ashby’s lifelong ‘hobbyhorse’.</p>
