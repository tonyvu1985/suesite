		<strong>Glomesh. Glamour in a handbag.</strong>
		<p>‘Glomesh’ is the trademark name for the iconic Australian metal mesh produced in metallic golds and silvers, and enamel creams, black and burgundy and used to make glamourous handbags, purses, clutches, cigarette holders, lighters, make up compacts, perfume atomisers and other luxurious lady essentials. And not forgetting the sexy and sinuous Glomesh halter neck top.</p>

		<h1 class="title">Mesh history </h1>
		<p>Originally produced by Glomesh International Pty Ltd in Bondi, Glomesh production moved to the May St factory in St Peters in the Sydney Marrickville Local Government Area in the 1960’s and 70’s. In 1974 the company was sold to Liberman Kolotex which manufactured Glomesh in George St, Leichhardt before the brand moved offshore and then disappeared in the 1980s.</p>

		<h1 class="title">Mish mesh</h1>
		<p>Metallic mesh was also used by Whiting and Davis in the USA to create handbags in the 1940s. In the 1950s Oroton imported metal mesh powder compacts from West Germany - sourced from a  producer of chain mail - before developing shiny mesh evening bags in the 70s – perfect for the disco!  And of course, there’s plenty of Chinese made ‘faux-mesh’ in circulation.</p>

		<h1 class="title">Re-mesh</h1>
		<p>While pre-loved mesh pieces are a staple find in vintage markets and op shops, the brand Glomesh relaunched in late 2013, making new and beautiful contemporary Glomesh bags.</p>

